/*
 * [PRG_Mock_09] 고대 유물 배낭 최적화 (Lv.3 수준)
 * - 훈련 날짜: 2026-06-22 (월)
 * - 알고리즘: 0-1 Knapsack (1차원 배낭 DP)
 * - 목적: 무게 한계가 정해진 배낭에 각 유물을 한 번씩만 담아 가치 총합을 최대로 구축
 * - 엔진 실무 최적화:
 * 1. O(K) 공간 최적화 : N * K 크기의 2차원 배열 대신 K 크기의 1차원 배열만 사용하여 메모리 점유율을 극단적으로 압축.
 * 2. 역방향 순회( Reverse Traversal ) : `dp` 배열 갱신 시 무게 K부터 현재 유물 무게까지 역방향으로 순회하여,
 동일한 유물이 중복해서 가방에 들어가는 Unbounded Knapsack 논리적 오류를 원천 차단함. 
 */

#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// =========================================================================
// [아키텍트 훈련 제출 구역]
// =========================================================================

int solution(vector<vector<int>> item, int k) {
    vector<int> dp(k + 1, 0);

    for (int i = 0; i < item.size(); i++)
    {
        for (int j = k; j >= item[i][0]; j--)
            dp[j] = max(dp[j], dp[j - item[i][0]] + item[i][1]);
    }

    return dp[k];
}
// =========================================================================

int main() {
    int k = 7; // 배낭의 최대 버티기 무게
    // {무게, 가치}
    vector<vector<int>> item = {
        {6, 13},
        {4, 8},
        {3, 6},
        {5, 12}
    };

    // 기대 정답 분석:
    // 유물 조합 1: 무게 6 단일 -> 총 가치 13
    // 유물 조합 2: 무게 5 단일 -> 총 가치 12
    // 유물 조합 3: 무게 4 + 무게 3 -> 총 무게 7, 총 가치 8 + 6 = 14 (최적해!)
    // 기대 반환값: 14

    int result = solution(item, k);

    cout << "========================================" << endl;
    cout << "[가방에 담을 수 있는 최대 가치] : " << result << " (기대값: 14)" << endl;
    cout << "========================================" << endl;

    return 0;
}